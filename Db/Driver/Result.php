declare(strict_types=1);

namespace VV\Db\Driver;

/**
 * Interface Result
 *
 * @package VV\Db\Driver
 */
interface Result
{
    /**
     * Returns fetch data iterator $flags
     *
     * @param int $flags Bit mask with self::FETCH_ constants
     *
     * @return \Traversable
     */
    public function getIterator(int $flags): \Traversable;

    /**
     * Returns autogenerated ID of last inserted row
     *
     * @return int|string|null
     */
    public function getInsertedId(): int|string|null;

    /**
     * Returns number of rows affected during statement execution
     *
     * @return int
     */
    public function getAffectedRows(): int;

    /**
     * Closes statement
     */
    public function close(): void;
}
